
CREATE TABLE toy (
	id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY ,
	name VARCHAR ( 50 ) UNIQUE NOT NULL,
	amount DECIMAL ,
	price DECIMAL  ,
	image_path VARCHAR(255)

);

CREATE TABLE product (
	id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	name VARCHAR ( 50 ) UNIQUE NOT NULL,
	amount DECIMAL ,
	price DECIMAL  ,
	image_path VARCHAR(255)

);

CREATE TABLE clothes (
	id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY ,
	name VARCHAR ( 50 ) UNIQUE NOT NULL,
	amount DECIMAL ,
	price DECIMAL  ,
	image_path VARCHAR(255)

);

CREATE TABLE food (
	id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	name VARCHAR ( 50 ) UNIQUE NOT NULL,
	amount DECIMAL ,
	price DECIMAL  ,
	image_path VARCHAR(255)

);

CREATE TABLE user (
	id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	name VARCHAR ( 50 ) UNIQUE NOT NULL,
	age DECIMAL ,
	salary DECIMAL


);